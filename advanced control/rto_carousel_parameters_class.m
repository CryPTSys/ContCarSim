    properties
        names_components
        ports_working
        cp_gas_components
        integration_interval
        control_interval
        V_slurry
        filtration_sampling_time
        number_components
        number_volatile_components    
        T_room
        visc_liq_coeff 
        MW_components
        cp
        rho_liq_components
        cp_liq_components
        wl_crit
